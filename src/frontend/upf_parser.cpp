std::vector<std::string> UpfChecker::check(const UpfDesign& upf, const Netlist& nl) {
    std::vector<std::string> issues;
    check_domain_supplies(upf, issues);
    check_isolation_refs(upf, issues);
    check_level_shifters(upf, issues);
    check_retention_coverage(upf, issues);
    check_supply_consistency(upf, issues);
    check_pst_validity(upf, issues);
    check_netlist_coverage(upf, nl, issues);
    return issues;
}

void UpfChecker::check_domain_supplies(const UpfDesign& upf,
                                       std::vector<std::string>& issues) {
    for (auto& [name, dom] : upf.domains) {
        if (dom.primary_power_net.empty())
            issues.push_back("[ERROR] Domain '" + name + "' has no primary power net");
        else if (upf.supply_nets.find(dom.primary_power_net) == upf.supply_nets.end())
            issues.push_back("[ERROR] Domain '" + name + "' power net '" +
                             dom.primary_power_net + "' is not defined");

        if (dom.primary_ground_net.empty())
            issues.push_back("[ERROR] Domain '" + name + "' has no primary ground net");
        else if (upf.supply_nets.find(dom.primary_ground_net) == upf.supply_nets.end())
            issues.push_back("[ERROR] Domain '" + name + "' ground net '" +
                             dom.primary_ground_net + "' is not defined");
    }
}

void UpfChecker::check_isolation_refs(const UpfDesign& upf,
                                      std::vector<std::string>& issues) {
    for (auto& iso : upf.isolations) {
        if (upf.domains.find(iso.domain) == upf.domains.end())
            issues.push_back("[ERROR] Isolation '" + iso.name +
                             "' references unknown domain '" + iso.domain + "'");
        if (!iso.isolation_power_net.empty() &&
            upf.supply_nets.find(iso.isolation_power_net) == upf.supply_nets.end())
            issues.push_back("[WARN] Isolation '" + iso.name +
                             "' power net '" + iso.isolation_power_net + "' is not defined");
        if (iso.isolation_signal.empty())
            issues.push_back("[WARN] Isolation '" + iso.name + "' has no isolation signal");
    }
}

void UpfChecker::check_level_shifters(const UpfDesign& upf,
                                      std::vector<std::string>& issues) {
    // Build set of domains that have level shifters
    std::unordered_set<std::string> domains_with_ls;
    for (auto& ls : upf.level_shifters) {
        if (upf.domains.find(ls.domain) == upf.domains.end())
            issues.push_back("[ERROR] Level shifter '" + ls.name +
                             "' references unknown domain '" + ls.domain + "'");
        domains_with_ls.insert(ls.domain);
    }

    // Check that domains at different voltages have level shifters
    for (auto& [n1, d1] : upf.domains) {
        for (auto& [n2, d2] : upf.domains) {
            if (n1 >= n2) continue; // avoid duplicates
            if (std::abs(d1.voltage - d2.voltage) > 0.01) {
                bool has_ls = domains_with_ls.count(n1) || domains_with_ls.count(n2);
                if (!has_ls)
                    issues.push_back("[WARN] Domains '" + n1 + "' (" +
                                     std::to_string(d1.voltage) + "V) and '" + n2 + "' (" +
                                     std::to_string(d2.voltage) + "V) have different voltages"
                                     " but no level shifter is defined");
            }
        }
    }
}

void UpfChecker::check_retention_coverage(const UpfDesign& upf,
                                          std::vector<std::string>& issues) {
    // Find domains that can be powered off
    std::unordered_set<std::string> can_power_off;
    for (auto& pst : upf.state_tables) {
        for (auto& st : pst.states) {
            for (auto& [sn, v] : st.supply_voltages) {
                if (v < 0) {
                    for (auto& [dname, dom] : upf.domains)
                        if (dom.primary_power_net == sn)
                            can_power_off.insert(dname);
                }
            }
        }
    }

    std::unordered_set<std::string> has_retention;
    for (auto& ret : upf.retentions) has_retention.insert(ret.domain);

    for (auto& d : can_power_off) {
        if (has_retention.find(d) == has_retention.end())
            issues.push_back("[WARN] Domain '" + d +
                             "' can be powered off but has no retention strategy");
    }
}

void UpfChecker::check_supply_consistency(const UpfDesign& upf,
                                          std::vector<std::string>& issues) {
    for (auto& [name, sn] : upf.supply_nets) {
        if (sn.voltage < 0)
            issues.push_back("[WARN] Supply '" + name + "' has negative voltage");
    }
    // Check domain voltages are positive
    for (auto& [name, dom] : upf.domains) {
        if (dom.voltage < 0)
            issues.push_back("[ERROR] Domain '" + name + "' has negative voltage");
    }
}

void UpfChecker::check_pst_validity(const UpfDesign& upf,
                                    std::vector<std::string>& issues) {
    for (auto& pst : upf.state_tables) {
        if (pst.supply_names.empty())
            issues.push_back("[WARN] PST '" + pst.name + "' has no supply columns defined");

        for (auto& sn : pst.supply_names) {
            if (upf.supply_nets.find(sn) == upf.supply_nets.end())
                issues.push_back("[WARN] PST '" + pst.name +
                                 "' references unknown supply '" + sn + "'");
        }

        if (pst.states.empty())
            issues.push_back("[WARN] PST '" + pst.name + "' has no states defined");

        // Check that all states cover all supply columns
        for (auto& st : pst.states) {
            for (auto& sn : pst.supply_names) {
                if (st.supply_voltages.find(sn) == st.supply_voltages.end())
                    issues.push_back("[WARN] PST '" + pst.name + "' state '" + st.name +
                                     "' does not specify supply '" + sn + "'");
            }
        }
    }
}

void UpfChecker::check_netlist_coverage(const UpfDesign& upf, const Netlist& nl,
                                        std::vector<std::string>& issues) {
    // Build a set of gate names from the netlist for cross-referencing
    std::unordered_set<std::string> nl_instances;
    for (auto& g : nl.gates())
        if (!g.name.empty()) nl_instances.insert(g.name);

    // Check that domain elements exist in the netlist
    for (auto& [dname, dom] : upf.domains) {
        for (auto& elem : dom.elements) {
            if (nl_instances.find(elem) == nl_instances.end())
                issues.push_back("[WARN] Domain '" + dname +
                                 "' element '" + elem + "' not found in netlist");
        }
    }

    // Check that non-top domains without elements have at least a warning
    for (auto& [dname, dom] : upf.domains) {
        if (!dom.is_top && dom.elements.empty())
            issues.push_back("[INFO] Domain '" + dname +
                             "' has no explicit element list");
    }

    // Check for isolation on domain boundaries
    std::unordered_set<std::string> isolated_domains;
    for (auto& iso : upf.isolations)
        isolated_domains.insert(iso.domain);

    for (auto& [dname, dom] : upf.domains) {
        if (dom.is_top) continue;
        if (isolated_domains.find(dname) == isolated_domains.end())
            issues.push_back("[WARN] Non-top domain '" + dname +
                             "' has no isolation strategy — signals crossing boundary"
                             " may be unprotected");
    }
}

