const PowerDomain* UpfDesign::find_domain(const std::string& name) const {
    auto it = domains.find(name);
    return it != domains.end() ? &it->second : nullptr;
}

const SupplyNet* UpfDesign::find_supply(const std::string& name) const {
    auto it = supply_nets.find(name);
    return it != supply_nets.end() ? &it->second : nullptr;
}

std::vector<std::string> UpfDesign::get_domain_hierarchy(const std::string& domain) const {
    std::vector<std::string> path;
    std::string cur = domain;
    std::unordered_set<std::string> visited;
    while (!cur.empty() && visited.find(cur) == visited.end()) {
        visited.insert(cur);
        path.push_back(cur);
        auto it = domains.find(cur);
        if (it == domains.end()) break;
        cur = it->second.parent_domain;
    }
    std::reverse(path.begin(), path.end());
    return path;
}

bool UpfDesign::validate() const {
    // We cast away const to reuse the mutable errors/warnings vectors.
    // A proper design would separate validation results; here we keep the
    // API surface clean.
    auto& self = const_cast<UpfDesign&>(*this);
    self.errors.clear();
    self.warnings.clear();

    // 1. Every domain must have primary power and ground
    for (auto& [name, dom] : domains) {
        if (dom.primary_power_net.empty())
            self.errors.push_back("Domain '" + name + "' has no primary power net");
        if (dom.primary_ground_net.empty())
            self.errors.push_back("Domain '" + name + "' has no primary ground net");
    }

    // 2. Isolation strategies must reference valid domains
    for (auto& iso : isolations) {
        if (domains.find(iso.domain) == domains.end())
            self.errors.push_back("Isolation '" + iso.name +
                                  "' references unknown domain '" + iso.domain + "'");
    }

    // 3. Level shifters must reference valid domains
    for (auto& ls : level_shifters) {
        if (domains.find(ls.domain) == domains.end())
            self.errors.push_back("Level shifter '" + ls.name +
                                  "' references unknown domain '" + ls.domain + "'");
    }

    // 4. Retention strategies must reference valid domains
    for (auto& ret : retentions) {
        if (domains.find(ret.domain) == domains.end())
            self.errors.push_back("Retention '" + ret.name +
                                  "' references unknown domain '" + ret.domain + "'");
    }

    // 5. PST supply names should exist
    for (auto& pst : state_tables) {
        for (auto& sn : pst.supply_names) {
            if (supply_nets.find(sn) == supply_nets.end())
                self.warnings.push_back("PST '" + pst.name +
                                        "' references unknown supply '" + sn + "'");
        }
    }

    // 6. Domains that can be powered off should have retention
    std::unordered_set<std::string> domains_with_off;
    for (auto& pst : state_tables) {
        for (auto& st : pst.states) {
            for (auto& [sn, v] : st.supply_voltages) {
                if (v < 0) { // OFF
                    // Find which domain owns this supply
                    for (auto& [dname, dom] : domains) {
                        if (dom.primary_power_net == sn)
                            domains_with_off.insert(dname);
                    }
                }
            }
        }
    }
    std::unordered_set<std::string> domains_with_retention;
    for (auto& ret : retentions)
        domains_with_retention.insert(ret.domain);
    for (auto& d : domains_with_off) {
        if (domains_with_retention.find(d) == domains_with_retention.end())
            self.warnings.push_back("Domain '" + d +
                                    "' can be powered off but has no retention strategy");
    }

    return self.errors.empty();
}

// ── to_upf() — export back to UPF text ─────────────────────────────────

static std::string brace_list(const std::vector<std::string>& v) {
    if (v.empty()) return "{}";
    if (v.size() == 1) return v[0];
    std::string s = "{";
    for (size_t i = 0; i < v.size(); ++i) {
        if (i) s += ' ';
        s += v[i];
    }
    s += '}';
    return s;
}

std::string UpfDesign::to_upf() const {
    std::ostringstream o;
    o << "# UPF " << version << " — generated by SiliconForge\n\n";

    // Domains
    for (auto& [name, dom] : domains) {
        o << "create_power_domain " << name;
        if (dom.is_top) o << " -include_scope";
        if (!dom.elements.empty()) o << " -elements " << brace_list(dom.elements);
        o << '\n';
    }
    o << '\n';

    // Supply nets
    for (auto& [name, sn] : supply_nets) {
        o << "create_supply_net " << name;
        // find owning domain
        for (auto& [dname, dom] : domains) {
            for (auto& s : dom.supply_nets) {
                if (s == name) { o << " -domain " << dname; goto done_sn; }
            }
        }
        done_sn:
        if (sn.is_resolved) o << " -resolve parallel";
        o << '\n';
    }
    o << '\n';

    // Supply ports
    for (auto& [name, sp] : supply_ports) {
        o << "create_supply_port " << name
          << " -domain " << sp.domain
          << " -direction " << (sp.direction == SupplyPort::IN ? "in" : "out")
          << '\n';
    }
    o << '\n';

    // Connect supply nets
    for (auto& [name, sn] : supply_nets) {
        if (!sn.connected_supply_port.empty())
            o << "connect_supply_net " << name
              << " -ports {" << sn.connected_supply_port << "}\n";
    }
    o << '\n';

    // Domain supply net assignments
    for (auto& [name, dom] : domains) {
        if (!dom.primary_power_net.empty() || !dom.primary_ground_net.empty()) {
            o << "set_domain_supply_net " << name;
            if (!dom.primary_power_net.empty())
                o << " -primary_power_net " << dom.primary_power_net;
            if (!dom.primary_ground_net.empty())
                o << " -primary_ground_net " << dom.primary_ground_net;
            o << '\n';
        }
    }
    o << '\n';

    // Isolation
    for (auto& iso : isolations) {
        o << "set_isolation " << iso.name << " -domain " << iso.domain;
        if (!iso.isolation_power_net.empty())
            o << " \\\n    -isolation_power_net " << iso.isolation_power_net;
        if (!iso.isolation_ground_net.empty())
            o << " \\\n    -isolation_ground_net " << iso.isolation_ground_net;
        o << " \\\n    -clamp_value " << static_cast<int>(iso.type);
        if (!iso.applies_to.empty())
            o << " \\\n    -applies_to " << iso.applies_to;
        if (!iso.isolation_signal.empty())
            o << " \\\n    -isolation_signal " << iso.isolation_signal;
        o << " \\\n    -sense " << (iso.sense == IsolationStrategy::HIGH ? "high" : "low");
        if (!iso.location.empty())
            o << " \\\n    -location " << iso.location;
        o << '\n';
    }
    o << '\n';

    // Level shifters
    for (auto& ls : level_shifters) {
        o << "set_level_shifter " << ls.name << " -domain " << ls.domain;
        if (!ls.applies_to.empty()) o << " \\\n    -applies_to " << ls.applies_to;
        if (!ls.rule.empty()) o << " \\\n    -rule " << ls.rule;
        if (!ls.location.empty()) o << " \\\n    -location " << ls.location;
        o << '\n';
    }
    o << '\n';

    // Retention
    for (auto& ret : retentions) {
        o << "set_retention " << ret.name << " -domain " << ret.domain;
        if (!ret.retention_power_net.empty())
            o << " \\\n    -retention_power_net " << ret.retention_power_net;
        if (!ret.save_signal.empty())
            o << " \\\n    -save_signal {" << ret.save_signal << "}";
        if (!ret.restore_signal.empty())
            o << " \\\n    -restore_signal {" << ret.restore_signal << "}";
        o << '\n';
    }
    o << '\n';

    // Power states
    for (auto& pst : state_tables) {
        o << "create_pst " << pst.name
          << " -supplies " << brace_list(pst.supply_names) << '\n';
        for (auto& st : pst.states) {
            o << "add_pst_state " << pst.name << '.' << st.name
              << " -pst " << pst.name << " -state {";
            for (size_t i = 0; i < pst.supply_names.size(); ++i) {
                if (i) o << ' ';
                auto it = st.supply_voltages.find(pst.supply_names[i]);
                if (it != st.supply_voltages.end() && it->second < 0)
                    o << "OFF";
                else
                    o << "ON";
            }
            o << "}\n";
        }
    }

    return o.str();
}

// ── report() — human-readable summary ───────────────────────────────────

std::string UpfDesign::report() const {
    std::ostringstream o;
    o << "=======================================\n"
      << " SiliconForge UPF Power Intent Report\n"
      << "=======================================\n";

    // Domains
    o << "Domains: " << domains.size() << " (";
    { bool first = true;
      for (auto& [n, _] : domains) { if (!first) o << ", "; o << n; first = false; }
    }
    o << ")\n";

    // Supplies
    o << "Supplies: " << supply_nets.size() << " (";
    { bool first = true;
      for (auto& [n, s] : supply_nets) {
          if (!first) o << ", ";
          o << n << '=' << s.voltage << 'V';
          first = false;
      }
    }
    o << ")\n";

    // Isolation
    o << "Isolation: " << isolations.size() << " strategies (";
    { bool first = true;
      for (auto& iso : isolations) { if (!first) o << ", "; o << iso.name; first = false; }
    }
    o << ")\n";

    // Level shifters
    o << "Level Shifters: " << level_shifters.size() << " (";
    { bool first = true;
      for (auto& ls : level_shifters) { if (!first) o << ", "; o << ls.name; first = false; }
    }
    o << ")\n";

    // Retention
    o << "Retention: " << retentions.size() << " (";
    { bool first = true;
      for (auto& r : retentions) { if (!first) o << ", "; o << r.name; first = false; }
    }
    o << ")\n";

    // Power states (count all states across tables)
    size_t total_states = 0;
    for (auto& pst : state_tables) total_states += pst.states.size();
    o << "Power States: " << total_states << " (";
    { bool first = true;
      for (auto& pst : state_tables)
          for (auto& st : pst.states) {
              if (!first) o << ", "; o << st.name; first = false;
          }
    }
    o << ")\n";

    // Validation
    auto& self = const_cast<UpfDesign&>(*this);
    bool ok = self.validate();
    o << "Validation: " << (ok ? "PASS" : "FAIL")
      << " (" << errors.size() << " errors, " << warnings.size() << " warnings)\n";

    return o.str();
}

// ════════════════════════════════════════════════════════════════════════
//  UpfParser — TCL-like tokeniser
// ════════════════════════════════════════════════════════════════════════

void UpfParser::error(UpfDesign& upf, int line, const std::string& msg) {
    upf.errors.push_back("line " + std::to_string(line) + ": " + msg);
}

void UpfParser::warn(UpfDesign& upf, int line, const std::string& msg) {
    upf.warnings.push_back("line " + std::to_string(line) + ": " + msg);
}

std::vector<UpfParser::Command> UpfParser::split_into_commands(const std::string& content) {
    std::vector<Command> cmds;
    std::istringstream stream(content);
    std::string logical_line;
    int line_num = 0;
    int cmd_start = 0;

    std::string raw;
    while (std::getline(stream, raw)) {
        ++line_num;
        // Strip trailing \r
        if (!raw.empty() && raw.back() == '\r') raw.pop_back();
        // Strip inline comments (not inside braces/quotes)
        {
            bool in_brace = false, in_quote = false;
            for (size_t i = 0; i < raw.size(); ++i) {
                if (raw[i] == '{') in_brace = true;
                else if (raw[i] == '}') in_brace = false;
                else if (raw[i] == '"') in_quote = !in_quote;
                else if (raw[i] == '#' && !in_brace && !in_quote) {
                    raw = raw.substr(0, i);
                    break;
                }
            }
        }
        // Handle backslash continuation
        if (!raw.empty() && raw.back() == '\\') {
            if (logical_line.empty()) cmd_start = line_num;
            logical_line += raw.substr(0, raw.size() - 1) + ' ';
            continue;
        }
        if (logical_line.empty()) cmd_start = line_num;
        logical_line += raw;

        // Trim
        auto start = logical_line.find_first_not_of(" \t");
        if (start != std::string::npos) {
            auto end = logical_line.find_last_not_of(" \t");
            std::string trimmed = logical_line.substr(start, end - start + 1);
            if (!trimmed.empty()) {
                Command cmd;
                cmd.args = tokenize(trimmed);
                cmd.line_number = cmd_start;
                if (!cmd.args.empty()) cmds.push_back(std::move(cmd));
            }
        }
        logical_line.clear();
    }
    // Flush any remaining continuation
    if (!logical_line.empty()) {
        auto start = logical_line.find_first_not_of(" \t");
        if (start != std::string::npos) {
            auto end = logical_line.find_last_not_of(" \t");
            Command cmd;
            cmd.args = tokenize(logical_line.substr(start, end - start + 1));
            cmd.line_number = cmd_start;
            if (!cmd.args.empty()) cmds.push_back(std::move(cmd));
        }
    }
    return cmds;
}

std::vector<std::string> UpfParser::tokenize(const std::string& line) {
    std::vector<std::string> tokens;
    std::string cur;
    int brace_depth = 0;
    bool in_quote = false;

    for (size_t i = 0; i < line.size(); ++i) {
        char c = line[i];

        if (in_quote) {
            if (c == '"') {
                in_quote = false;
                if (!cur.empty()) { tokens.push_back(cur); cur.clear(); }
            } else {
                cur += c;
            }
            continue;
        }

        if (brace_depth > 0) {
            if (c == '{') {
                ++brace_depth;
                cur += c;
            } else if (c == '}') {
                --brace_depth;
                if (brace_depth == 0) {
                    // Emit the brace-grouped content as a single token
                    tokens.push_back(cur);
                    cur.clear();
                } else {
                    cur += c;
                }
            } else {
                cur += c;
            }
            continue;
        }

        if (c == '"') {
            in_quote = true;
            if (!cur.empty()) { tokens.push_back(cur); cur.clear(); }
        } else if (c == '{') {
            if (!cur.empty()) { tokens.push_back(cur); cur.clear(); }
            ++brace_depth;
        } else if (std::isspace(static_cast<unsigned char>(c))) {
            if (!cur.empty()) { tokens.push_back(cur); cur.clear(); }
        } else {
            cur += c;
        }
    }
    if (!cur.empty()) tokens.push_back(cur);
    return tokens;
}

// ── Flag extraction helpers ─────────────────────────────────────────────

std::string UpfParser::get_flag(const std::vector<std::string>& args,
                                const std::string& flag,
                                const std::string& default_val) {
    for (size_t i = 0; i + 1 < args.size(); ++i)
        if (args[i] == flag) return args[i + 1];
    return default_val;
}

std::vector<std::string> UpfParser::get_flag_list(const std::vector<std::string>& args,
                                                  const std::string& flag) {
    std::string raw = get_flag(args, flag, "");
    if (raw.empty()) return {};
    // The value may be a single token or a space-separated brace group
    std::vector<std::string> out;
    std::istringstream ss(raw);
    std::string tok;
    while (ss >> tok) out.push_back(tok);
    return out;
}

bool UpfParser::has_flag(const std::vector<std::string>& args,
                         const std::string& flag) {
    for (auto& a : args)
        if (a == flag) return true;
    return false;
}

