UpfDesign UpfParser::parse_file(const std::string& filename) {
    std::ifstream f(filename);
    if (!f.is_open()) {
        UpfDesign upf;
        upf.errors.push_back("Cannot open file: " + filename);
        return upf;
    }
    std::ostringstream ss;
    ss << f.rdbuf();
    return parse_string(ss.str());
}

UpfDesign UpfParser::parse_string(const std::string& content) {
    UpfDesign upf;
    auto cmds = split_into_commands(content);

    for (auto& cmd : cmds) {
        if (cmd.args.empty() || cmd.args[0][0] == '#') continue;

        const std::string& name = cmd.args[0];
        if      (name == "create_power_domain")  parse_create_power_domain(cmd.args, upf, cmd.line_number);
        else if (name == "create_supply_net")     parse_create_supply_net(cmd.args, upf, cmd.line_number);
        else if (name == "create_supply_port")    parse_create_supply_port(cmd.args, upf, cmd.line_number);
        else if (name == "connect_supply_net")    parse_connect_supply_net(cmd.args, upf, cmd.line_number);
        else if (name == "set_domain_supply_net") parse_set_domain_supply_net(cmd.args, upf, cmd.line_number);
        else if (name == "set_isolation")         parse_set_isolation(cmd.args, upf, cmd.line_number);
        else if (name == "set_level_shifter")     parse_set_level_shifter(cmd.args, upf, cmd.line_number);
        else if (name == "set_retention")         parse_set_retention(cmd.args, upf, cmd.line_number);
        else if (name == "add_power_state")       parse_add_power_state(cmd.args, upf, cmd.line_number);
        else if (name == "create_pst")            parse_create_pst(cmd.args, upf, cmd.line_number);
        else if (name == "add_pst_state")         parse_add_pst_state(cmd.args, upf, cmd.line_number);
        // Skip unknown commands gracefully
    }
    return upf;
}

