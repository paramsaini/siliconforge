void UpfParser::parse_create_power_domain(const std::vector<std::string>& args,
                                          UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "create_power_domain: missing domain name"); return; }
    std::string name = args[1];
    PowerDomain dom;
    dom.name = name;
    dom.is_top = has_flag(args, "-include_scope");
    dom.elements = get_flag_list(args, "-elements");

    std::string parent = get_flag(args, "-scope");
    if (!parent.empty()) dom.parent_domain = parent;

    double v = 0;
    std::string vs = get_flag(args, "-voltage");
    if (!vs.empty()) {
        try { v = std::stod(vs); } catch (...) {}
        dom.voltage = v;
    }

    upf.domains[name] = std::move(dom);
}

void UpfParser::parse_create_supply_net(const std::vector<std::string>& args,
                                        UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "create_supply_net: missing net name"); return; }
    std::string name = args[1];
    SupplyNet sn;
    sn.name = name;
    sn.is_resolved = has_flag(args, "-resolve");

    std::string vs = get_flag(args, "-voltage");
    if (!vs.empty()) {
        try { sn.voltage = std::stod(vs); } catch (...) {}
    }

    upf.supply_nets[name] = std::move(sn);

    // Associate with domain
    std::string domain = get_flag(args, "-domain");
    if (!domain.empty()) {
        auto it = upf.domains.find(domain);
        if (it != upf.domains.end())
            it->second.supply_nets.push_back(name);
        else
            warn(upf, line, "create_supply_net: domain '" + domain + "' not yet defined");
    }
}

void UpfParser::parse_create_supply_port(const std::vector<std::string>& args,
                                         UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "create_supply_port: missing port name"); return; }
    SupplyPort sp;
    sp.name = args[1];
    sp.domain = get_flag(args, "-domain");
    std::string dir = get_flag(args, "-direction", "in");
    sp.direction = (dir == "out") ? SupplyPort::OUT : SupplyPort::IN;
    upf.supply_ports[sp.name] = std::move(sp);
}

void UpfParser::parse_connect_supply_net(const std::vector<std::string>& args,
                                         UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "connect_supply_net: missing net name"); return; }
    std::string net_name = args[1];
    auto ports = get_flag_list(args, "-ports");

    auto it = upf.supply_nets.find(net_name);
    if (it == upf.supply_nets.end()) {
        warn(upf, line, "connect_supply_net: supply net '" + net_name + "' not defined");
        return;
    }
    if (!ports.empty())
        it->second.connected_supply_port = ports[0];
}

void UpfParser::parse_set_domain_supply_net(const std::vector<std::string>& args,
                                            UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "set_domain_supply_net: missing domain name"); return; }
    std::string domain = args[1];
    auto it = upf.domains.find(domain);
    if (it == upf.domains.end()) {
        error(upf, line, "set_domain_supply_net: unknown domain '" + domain + "'");
        return;
    }
    std::string ppn = get_flag(args, "-primary_power_net");
    std::string pgn = get_flag(args, "-primary_ground_net");
    if (!ppn.empty()) it->second.primary_power_net = ppn;
    if (!pgn.empty()) it->second.primary_ground_net = pgn;

    // Propagate voltage from supply net
    if (!ppn.empty()) {
        auto sn = upf.supply_nets.find(ppn);
        if (sn != upf.supply_nets.end() && sn->second.voltage > 0)
            it->second.voltage = sn->second.voltage;
    }
}

void UpfParser::parse_set_isolation(const std::vector<std::string>& args,
                                    UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "set_isolation: missing strategy name"); return; }
    IsolationStrategy iso;
    iso.name = args[1];
    iso.domain = get_flag(args, "-domain");
    iso.isolation_power_net = get_flag(args, "-isolation_power_net");
    iso.isolation_ground_net = get_flag(args, "-isolation_ground_net");
    iso.applies_to = get_flag(args, "-applies_to", "outputs");
    iso.isolation_signal = get_flag(args, "-isolation_signal");
    iso.location = get_flag(args, "-location", "parent");

    // Clamp value
    std::string cv = get_flag(args, "-clamp_value", "0");
    if      (cv == "0")     iso.type = IsolationStrategy::CLAMP_0;
    else if (cv == "1")     iso.type = IsolationStrategy::CLAMP_1;
    else if (cv == "latch") iso.type = IsolationStrategy::LATCH;

    // Sense
    std::string sense = get_flag(args, "-sense", "high");
    iso.sense = (sense == "low") ? IsolationStrategy::LOW : IsolationStrategy::HIGH;

    if (iso.domain.empty())
        warn(upf, line, "set_isolation '" + iso.name + "': no -domain specified");

    upf.isolations.push_back(std::move(iso));
}

void UpfParser::parse_set_level_shifter(const std::vector<std::string>& args,
                                        UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "set_level_shifter: missing name"); return; }
    LevelShifter ls;
    ls.name = args[1];
    ls.domain = get_flag(args, "-domain");
    ls.applies_to = get_flag(args, "-applies_to", "both");
    ls.rule = get_flag(args, "-rule", "both");
    ls.location = get_flag(args, "-location", "self");
    ls.threshold = get_flag(args, "-threshold");

    std::string iv = get_flag(args, "-input_voltage");
    std::string ov = get_flag(args, "-output_voltage");
    if (!iv.empty()) { try { ls.input_voltage = std::stod(iv); } catch (...) {} }
    if (!ov.empty()) { try { ls.output_voltage = std::stod(ov); } catch (...) {} }

    if (ls.domain.empty())
        warn(upf, line, "set_level_shifter '" + ls.name + "': no -domain specified");

    upf.level_shifters.push_back(std::move(ls));
}

void UpfParser::parse_set_retention(const std::vector<std::string>& args,
                                    UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "set_retention: missing name"); return; }
    RetentionStrategy ret;
    ret.name = args[1];
    ret.domain = get_flag(args, "-domain");
    ret.retention_power_net = get_flag(args, "-retention_power_net");
    ret.retention_ground_net = get_flag(args, "-retention_ground_net");

    // Save/restore signals may be brace-grouped
    auto save_list = get_flag_list(args, "-save_signal");
    if (!save_list.empty()) ret.save_signal = save_list[0];
    auto restore_list = get_flag_list(args, "-restore_signal");
    if (!restore_list.empty()) ret.restore_signal = restore_list[0];

    ret.elements = get_flag_list(args, "-elements");

    std::string sc = get_flag(args, "-save_condition", "edge");
    ret.save_condition = (sc == "level") ? RetentionStrategy::LEVEL : RetentionStrategy::EDGE;

    if (ret.domain.empty())
        warn(upf, line, "set_retention '" + ret.name + "': no -domain specified");

    upf.retentions.push_back(std::move(ret));
}

void UpfParser::parse_add_power_state(const std::vector<std::string>& args,
                                      UpfDesign& upf, int line) {
    // Syntax: add_power_state DOMAIN.state -supply {NET voltage} [-supply ...] -simstate STATE
    if (args.size() < 2) { error(upf, line, "add_power_state: missing state name"); return; }

    std::string qualified = args[1]; // e.g. "PD_TOP.active"
    std::string domain_name, state_name;
    auto dot = qualified.find('.');
    if (dot != std::string::npos) {
        domain_name = qualified.substr(0, dot);
        state_name = qualified.substr(dot + 1);
    } else {
        state_name = qualified;
    }

    PowerState ps;
    ps.name = state_name;
    ps.simstate = get_flag(args, "-simstate", "NORMAL");

    // Collect all -supply flags (there may be multiple)
    for (size_t i = 0; i + 1 < args.size(); ++i) {
        if (args[i] == "-supply") {
            // Value is brace-grouped: "NET voltage" or "NET OFF"
            std::string raw = args[i + 1];
            std::istringstream ss(raw);
            std::string sname, sval;
            if (ss >> sname >> sval) {
                if (sval == "OFF" || sval == "off")
                    ps.supply_voltages[sname] = -1.0;
                else {
                    try { ps.supply_voltages[sname] = std::stod(sval); } catch (...) {
                        ps.supply_voltages[sname] = 0.0;
                    }
                }
                // Also update the supply net's voltage if it's a resolved value
                if (ps.supply_voltages[sname] > 0) {
                    auto it = upf.supply_nets.find(sname);
                    if (it != upf.supply_nets.end() && it->second.voltage == 0.0)
                        it->second.voltage = ps.supply_voltages[sname];
                }
            }
        }
    }

    // Find or create a state table for this domain
    if (!domain_name.empty()) {
        // Look for an existing implicit state table for this domain
        bool found = false;
        for (auto& pst : upf.state_tables) {
            if (pst.name == domain_name + "_pst") {
                pst.states.push_back(std::move(ps));
                found = true;
                break;
            }
        }
        if (!found) {
            PowerStateTable pst;
            pst.name = domain_name + "_pst";
            // Collect supply names from the power state
            for (auto& [sn, _] : ps.supply_voltages)
                pst.supply_names.push_back(sn);
            pst.states.push_back(std::move(ps));
            upf.state_tables.push_back(std::move(pst));
        }
    }
}

void UpfParser::parse_create_pst(const std::vector<std::string>& args,
                                 UpfDesign& upf, int line) {
    if (args.size() < 2) { error(upf, line, "create_pst: missing PST name"); return; }
    PowerStateTable pst;
    pst.name = args[1];
    pst.supply_names = get_flag_list(args, "-supplies");
    upf.state_tables.push_back(std::move(pst));
}

void UpfParser::parse_add_pst_state(const std::vector<std::string>& args,
                                    UpfDesign& upf, int line) {
    // Syntax: add_pst_state pst1.s1 -pst pst1 -state {ON OFF}
    if (args.size() < 2) { error(upf, line, "add_pst_state: missing state name"); return; }

    std::string qualified = args[1];
    std::string state_name;
    auto dot = qualified.find('.');
    state_name = (dot != std::string::npos) ? qualified.substr(dot + 1) : qualified;

    std::string pst_name = get_flag(args, "-pst");
    auto state_vals = get_flag_list(args, "-state");

    // Find the PST
    PowerStateTable* target = nullptr;
    for (auto& pst : upf.state_tables) {
        if (pst.name == pst_name) { target = &pst; break; }
    }
    if (!target) {
        error(upf, line, "add_pst_state: PST '" + pst_name + "' not found");
        return;
    }

    PowerState ps;
    ps.name = state_name;
    for (size_t i = 0; i < state_vals.size() && i < target->supply_names.size(); ++i) {
        if (state_vals[i] == "ON" || state_vals[i] == "on")
            ps.supply_voltages[target->supply_names[i]] = 1.0; // nominal
        else if (state_vals[i] == "OFF" || state_vals[i] == "off")
            ps.supply_voltages[target->supply_names[i]] = -1.0;
        else {
            try { ps.supply_voltages[target->supply_names[i]] = std::stod(state_vals[i]); }
            catch (...) { ps.supply_voltages[target->supply_names[i]] = 0.0; }
        }
    }
    target->states.push_back(std::move(ps));
}

